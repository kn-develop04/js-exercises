import { fib } from "./index.js";
describe("フィボナッチ数列の関数", () => {
  describe("関数が正常に動く(小さい値)", () => {
    it("5を引数にしたとき、5を返すこと", () => {
      expect(fib(5)).toBe(5);
    });
  });
  describe("関数が正常に動く(大きい値)", () => {
    it("75を引数にしたとき、2111485077978050を返すこと", () => {
      expect(fib(75)).toBe(2111485077978050);
    });
  });
});
